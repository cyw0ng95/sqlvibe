func TestSQL1999_F421_NChar_L1(t *testing.T) {
	sqlvibePath := ":memory:"
	sqlitePath := ":memory:"

	sqlvibeDB, err := sqlvibe.Open(sqlvibePath)
	if err != nil {
		t.Fatalf("Failed to open sqlvibe: %v", err)
	}
	defer sqlvibeDB.Close()

	sqliteDB, err := sql.Open("sqlite", sqlitePath)
	if err != nil {
		t.Fatalf("Failed to open sqlite: %v", err)
	}
	defer sqliteDB.Close()

	setup := []struct {
		name string
		sql  string
	}{
		{"CreateTable", "CREATE TABLE t1 (a TEXT, b TEXT)"},
		{"InsertASCII", "INSERT INTO t1 VALUES ('hello', 'world')"},
		{"InsertUnicode", "INSERT INTO t1 VALUES ('你好', '世界')"},
		{"InsertMixed", "INSERT INTO t1 VALUES ('hello世界', 'test')"},
	}

	for _, tt := range setup {
		t.Run(tt.name, func(t *testing.T) {
			SQL1999.CompareExecResults(t, sqlvibeDB, sqliteDB, tt.sql, tt.name)
		})
	}

	queryTests := []struct {
		name string
		sql  string
	}{
		{"SelectAll", "SELECT * FROM t1"},
		{"SelectAscii", "SELECT * FROM t1 WHERE a = 'hello'"},
		{"SelectUnicode", "SELECT * FROM t1 WHERE a = '你好'"},
		{"SelectLike", "SELECT * FROM t1 WHERE a LIKE 'h%'"},
		{"Concat", "SELECT a || b FROM t1 WHERE a = 'hello'"},
		{"Length", "SELECT LENGTH(a), LENGTH(b) FROM t1 WHERE a = 'hello'"},
	}

	for _, tt := range queryTests {
		t.Run(tt.name, func(t *testing.T) {
			SQL1999.CompareQueryResults(t, sqlvibeDB, sqliteDB, tt.sql, tt.name)
		})
	}
}

