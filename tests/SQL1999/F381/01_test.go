package F381

import (
	"database/sql"

	_ "github.com/cyw0ng95/sqlvibe/driver"
	"testing"

	"github.com/cyw0ng95/sqlvibe/tests/SQL1999"
)

func TestSQL1999_F301_F38101_L1(t *testing.T) {
	sqlvibePath := ":memory:"
	sqlitePath := ":memory:"

	sqlvibeDB, err := sql.Open("sqlvibe", sqlvibePath)
	if err != nil {
		t.Fatalf("Failed to open sqlvibe: %v", err)
	}
	defer sqlvibeDB.Close()

	sqliteDB, err := sql.Open("sqlite", sqlitePath)
	if err != nil {
		t.Fatalf("Failed to open sqlite: %v", err)
	}
	defer sqliteDB.Close()

	tests := []struct {
		name string
		sql  string
	}{
		{"UniqueConstraint", "CREATE TABLE t1 (a INTEGER UNIQUE)"},
		{"NotNullConstraint", "CREATE TABLE t2 (a INTEGER NOT NULL)"},
		{"PrimaryKey", "CREATE TABLE t3 (a INTEGER PRIMARY KEY)"},
		{"CheckConstraint", "CREATE TABLE t4 (a INTEGER CHECK (a > 0))"},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			SQL1999.CompareExecResults(t, sqlvibeDB, sqliteDB, tt.sql, tt.name)
		})
	}
}
